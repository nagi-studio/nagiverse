import { appendFileSync, existsSync } from 'node:fs';
import { join } from 'node:path';
import process from 'node:process';

const COMMENT_MARKER = '<!-- content-pr-gate -->';

const AD_PATTERNS = [
	/(加微|加v|vx[:：]?|微信[:：]?|私信咨询|扫码领取|点击购买|下单|限时优惠|返利|代理|招商|推广合作|课程咨询|免费领取)/i,
	/(contact me for discount|limited offer|buy now|affiliate|referral code|dm me to join)/i
];

const POLITICAL_PATTERNS = [
	/(政治宣传|竞选动员|拉票|政党宣传|意识形态动员|政治煽动)/i,
	/(campaign for|vote for|political propaganda|election mobilization)/i
];

const MALICIOUS_PATTERNS = [
	/(钓鱼网站|窃取(密码|cookie|凭证)|勒索软件|后门程序|木马程序|免杀|远控|DDoS\s*攻击|恶意脚本)/i,
	/(phishing kit|credential stealing|ransomware|backdoor|trojan|steal cookies|steal credentials|malware payload)/i
];

const AI_AUTHORSHIP_PATTERNS = [
	/(本文|这篇|本篇).{0,12}(由|使用).{0,8}(AI|ChatGPT|Claude|GPT)/i,
	/(由|使用).{0,8}(AI|ChatGPT|Claude|GPT).{0,12}(生成|撰写|整理|改写)/i,
	/作为.?AI/i,
	/this article was generated by (ai|chatgpt|claude|gpt)/i
];

const PLACEHOLDER_PATTERNS = [/\bTODO\b/i, /\bTBD\b/i, /待补充|稍后补充|占位内容/];

function getRequiredEnv(name) {
	const value = process.env[name];
	if (!value) {
		throw new Error(`Missing required environment variable: ${name}`);
	}
	return value;
}

const token = getRequiredEnv('GITHUB_TOKEN');
const repository = getRequiredEnv('GITHUB_REPOSITORY');
const prNumber = Number(getRequiredEnv('PR_NUMBER'));
const workspace = process.env.GITHUB_WORKSPACE || process.cwd();

function isProfilePath(filePath) {
	return /^content\/users\/[^/]+\/profile\.ya?ml$/.test(filePath);
}

function isNotePath(filePath) {
	return /^content\/users\/[^/]+\/notes\/[^/]+\.md$/.test(filePath);
}

function isAllowedKnowledgePath(filePath) {
	return isProfilePath(filePath) || isNotePath(filePath);
}

function getUserIdFromPath(filePath) {
	const match = filePath.match(/^content\/users\/([^/]+)\//);
	return match?.[1] ?? null;
}

function stripWhitespace(text) {
	return text.replace(/\s+/g, '');
}

function splitFrontmatter(source) {
	const normalized = source.replace(/\r\n/g, '\n');
	const match = normalized.match(/^---\n([\s\S]*?)\n---\n?([\s\S]*)$/);
	if (!match) {
		return { frontmatter: '', body: normalized };
	}
	return { frontmatter: match[1], body: match[2] };
}

function hasSourceReference(frontmatter, body) {
	return (
		/^\s*source_url\s*:/m.test(frontmatter) ||
		/^\s*links\s*:/m.test(frontmatter) ||
		/https?:\/\/[^\s)]+/i.test(body)
	);
}

function firstMatchingReason(text) {
	const rules = [
		{ patterns: AI_AUTHORSHIP_PATTERNS, reason: '内容疑似直接由 AI 生成，仓库不接受此类投稿。' },
		{ patterns: AD_PATTERNS, reason: '内容疑似包含广告、导流或营销文案。' },
		{ patterns: POLITICAL_PATTERNS, reason: '内容疑似包含政治宣传或政治动员信息。' },
		{ patterns: MALICIOUS_PATTERNS, reason: '内容疑似包含恶意行为、攻击或窃取相关内容。' }
	];

	for (const rule of rules) {
		if (rule.patterns.some((pattern) => pattern.test(text))) {
			return rule.reason;
		}
	}

	return null;
}

function getBaseRelativePath(filePath) {
	return join(workspace, filePath);
}

function hasBaseProfile(userId) {
	return (
		existsSync(getBaseRelativePath(`content/users/${userId}/profile.yaml`)) ||
		existsSync(getBaseRelativePath(`content/users/${userId}/profile.yml`))
	);
}

function encodeContentPath(filePath) {
	return filePath
		.split('/')
		.map((part) => encodeURIComponent(part))
		.join('/');
}

async function github(path, init = {}) {
	const response = await fetch(`https://api.github.com${path}`, {
		...init,
		headers: {
			Authorization: `Bearer ${token}`,
			Accept: 'application/vnd.github+json',
			'X-GitHub-Api-Version': '2022-11-28',
			...(init.headers ?? {})
		}
	});

	if (!response.ok) {
		const text = await response.text();
		throw new Error(`GitHub API ${response.status} ${response.statusText}: ${text}`);
	}

	if (response.status === 204) {
		return null;
	}

	return response.json();
}

async function paginate(path) {
	const items = [];
	let page = 1;

	while (true) {
		const separator = path.includes('?') ? '&' : '?';
		const batch = await github(`${path}${separator}per_page=100&page=${page}`);
		items.push(...batch);
		if (batch.length < 100) {
			break;
		}
		page += 1;
	}

	return items;
}

async function getPullRequest() {
	return github(`/repos/${repository}/pulls/${prNumber}`);
}

async function getPullRequestFiles() {
	return paginate(`/repos/${repository}/pulls/${prNumber}/files`);
}

async function getFileContent(filePath, ref) {
	const payload = await github(
		`/repos/${repository}/contents/${encodeContentPath(filePath)}?ref=${encodeURIComponent(ref)}`
	);

	if (!payload?.content) {
		return '';
	}

	return Buffer.from(payload.content, 'base64').toString('utf-8');
}

async function upsertComment(body) {
	const comments = await github(`/repos/${repository}/issues/${prNumber}/comments?per_page=100`);
	const current = comments.find(
		(comment) =>
			comment.user?.login === 'github-actions[bot]' && typeof comment.body === 'string' && comment.body.includes(COMMENT_MARKER)
	);

	if (current) {
		await github(`/repos/${repository}/issues/comments/${current.id}`, {
			method: 'PATCH',
			body: JSON.stringify({ body })
		});
		return;
	}

	await github(`/repos/${repository}/issues/${prNumber}/comments`, {
		method: 'POST',
		body: JSON.stringify({ body })
	});
}

function writeOutput(name, value) {
	if (!process.env.GITHUB_OUTPUT) return;
	appendFileSync(process.env.GITHUB_OUTPUT, `${name}=${value}\n`);
}

function writeSummary(text) {
	if (!process.env.GITHUB_STEP_SUMMARY) return;
	appendFileSync(process.env.GITHUB_STEP_SUMMARY, `${text}\n`);
}

function formatList(items) {
	return items.map((item) => `- ${item}`).join('\n');
}

function buildComment({ statusText, intro, blockingIssues, attentionItems, skipReasons }) {
	const sections = [COMMENT_MARKER, '## Content Review Gate', `状态：${statusText}`, '', intro];

	if (blockingIssues.length > 0) {
		sections.push('', '需要修改：', formatList(blockingIssues));
	}

	if (attentionItems.length > 0) {
		sections.push('', '提醒事项：', formatList(attentionItems));
	}

	if (skipReasons.length > 0) {
		sections.push('', '未自动合并原因：', formatList(skipReasons));
	}

	return sections.join('\n');
}

async function main() {
	const pr = await getPullRequest();
	const files = await getPullRequestFiles();

	const blockingIssues = [];
	const attentionItems = [];
	const skipReasons = [];

	const noteFiles = files.filter((file) => isNotePath(file.filename) && file.status !== 'removed');
	const touchedUsers = new Set();
	const changedProfiles = new Set();

	for (const file of files) {
		const userId = getUserIdFromPath(file.filename);
		if (!userId) continue;
		touchedUsers.add(userId);
		if (isProfilePath(file.filename) && file.status !== 'removed') {
			changedProfiles.add(userId);
		}
	}

	const pureKnowledgePr =
		files.length > 0 &&
		files.every(
			(file) =>
				isAllowedKnowledgePath(file.filename) &&
				file.status !== 'removed' &&
				file.status !== 'renamed'
		);

	if (!pureKnowledgePr) {
		skipReasons.push('PR 包含非知识内容文件，或包含删除 / 重命名操作，保留人工处理。');
	}

	if (pr.draft) {
		skipReasons.push('PR 仍处于 draft 状态。');
	}

	for (const userId of touchedUsers) {
		const touchedNotes = noteFiles.some((file) => getUserIdFromPath(file.filename) === userId);
		if (!touchedNotes) continue;

		const baseProfileExists = hasBaseProfile(userId);
		if (!baseProfileExists && !changedProfiles.has(userId)) {
			blockingIssues.push(
				`检测到用户目录 \`content/users/${userId}\` 下有笔记变更，但没有对应的 \`profile.yaml\`。请先创建用户资料。`
			);
		}
	}

	for (const file of noteFiles) {
		const content = await getFileContent(file.filename, pr.head.sha);
		const { frontmatter, body } = splitFrontmatter(content);
		const compactBody = stripWhitespace(body);
		const fullText = `${frontmatter}\n${body}`;

		const blockedReason = firstMatchingReason(fullText);
		if (blockedReason) {
			blockingIssues.push(`\`${file.filename}\`: ${blockedReason}`);
		}

		if (PLACEHOLDER_PATTERNS.some((pattern) => pattern.test(body))) {
			blockingIssues.push(`\`${file.filename}\`: 正文里还有占位内容，不能直接合并。`);
		}

		if (file.status === 'added' && compactBody.length < 60) {
			blockingIssues.push(`\`${file.filename}\`: 正文过短，当前更像占位稿而不是完整的知识分享。`);
		}

		if (!hasSourceReference(frontmatter, body)) {
			attentionItems.push(`\`${file.filename}\`: 建议补充 \`source_url\`、frontmatter \`links\` 或正文出处。`);
		}
	}

	const shouldEnableAutoMerge =
		blockingIssues.length === 0 &&
		attentionItems.length === 0 &&
		skipReasons.length === 0 &&
		pureKnowledgePr;

	let statusText = '保留人工处理';
	let intro = '这次 PR 没有触发自动合并。';

	if (blockingIssues.length > 0) {
		statusText = '需要修改';
		intro = '检测到必须先修正的问题，已阻止自动合并。';
	} else if (shouldEnableAutoMerge) {
		statusText = '通过，准备启用 auto-merge';
		intro = '未发现显性风险，且本次改动属于纯知识内容，流程会为此 PR 启用 GitHub auto-merge。';
	} else if (attentionItems.length > 0) {
		statusText = '已提醒，暂不自动合并';
		intro = '内容基本可读，但还有需要补齐的注意点，当前不会自动合并。';
	}

	await upsertComment(
		buildComment({
			statusText,
			intro,
			blockingIssues,
			attentionItems,
			skipReasons
		})
	);

	writeOutput('enable_auto_merge', shouldEnableAutoMerge ? 'true' : 'false');
	writeOutput(
		'outcome',
		blockingIssues.length > 0 ? 'blocked' : shouldEnableAutoMerge ? 'eligible' : 'manual'
	);

	writeSummary(`## Content Review Gate\n\n${intro}`);

	if (blockingIssues.length > 0) {
		console.error(blockingIssues.join('\n'));
		process.exit(1);
	}
}

main().catch((error) => {
	console.error(error);
	process.exit(1);
});
